
public class Count_occurance_of_given_character_in_a_string {

	public static void main(String[] args) {
		String rv="abinaya";
		int count=0;
		for(int i=0;i<rv.length();i++)
		{
			if(rv.charAt(i)=='a')
				
			{
				count++;				
			}
	}

			
		System.out.println("character A occured no of times in a given string :" + count);
	}
}



